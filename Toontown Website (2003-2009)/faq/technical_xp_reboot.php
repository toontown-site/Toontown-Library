      <font size="2" face="Arial, Helvetica, sans-serif"><strong><em><font color="#0000FF">Q:</font> Windows
      XP spontaneously rebooted while I was playing Toontown.</em></strong></font>
      <p><em><strong><font size="2" face="Arial, Helvetica, sans-serif"><font color="#0000FF">A:</font> </font></strong><font size="2" face="Arial, Helvetica, sans-serif"></font></em><font size="2" face="Arial, Helvetica, sans-serif">Windows
          XP crashed, and by default XP is set to reboot itself automatically
          after an OS crash. You can change this setting under Control Panel-&gt;System-&gt;Advanced-&gt;Startup
          And Recovery. Because of its design, WinXP can only crash due to a
          hardware problem (such as bad memory chips) or a device driver problem.
          Device drivers are the usual suspects. Toontown exercises the video
          driver heavily, so you will want to make sure you have installed the
          latest video drivers from your graphics card manufacturer, and the
          latest motherboard drivers from your PC or motherboard manufacturer
          (i.e. Dell, HP, Intel, Asus, Via etc).</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">You can test your
          RAM memory integrity using a free utility at <a href="http://www.memtest86.com">http://www.memtest86.com</a>. </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">If none of these
          things work you should report the failure to your video card and motherboard
          manufacturer's customer support services. </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">For programmers and
          technically advanced users:</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">By default, after
          crashes WinXP writes a &quot;minidump&quot; file to C:\Windows\Minidump\mini[date].dmp.
          Looking at the strings in the file can sometimes give you a clue which
          driver crashed; for instance, the presence of 'nv4_disp.dll' indicates
          a crash in the Nvidia video driver. This does not necessary implicate
          the video driver (the crash could be due to a motherboard driver that
          the video driver depends on, or bad memory for instance), but it can
          be a starting point for investigation and driver swapping. You can
          load a minidump file using the windbg.exe Tool in the &quot;Debugging
          Tools for Windows&quot; package downloadable from <a href="http://msdn.microsoft.com">http://msdn.microsoft.com</a>.</font></p>
