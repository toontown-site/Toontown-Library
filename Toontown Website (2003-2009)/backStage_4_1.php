This issue of Backstage puts the focus on fishing! Once just a relaxing easy way to pass time in the playground, fishing will now require precision and expertise -- not to mention gadgets aplenty and bonus healing power! <br><br>
Our artists are designing a whole slew of new creatures to swim in the ponds. Models are currently being built for some of the new fish species. Once a fish has been caught, you can choose whether to throw it back in the water or take it to your estate to keep as a pet. You will even be able to take a dip with your new fishy friends.<br><br>
<img src="http://play.toontown.com/shared/images/TTBS_graphic_04_01.jpg" width="200" height="156" alt="" border="0">
