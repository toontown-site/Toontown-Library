</ul>

<b>August 2, 2006 [sv1.0.21.9]</b>
<ul>
<li>Six Estate cannons will be available for a 48 hour rental period in your next issue of Clarabelle's Cattlelog. Play in pinball mode, where a high score is awarded for careening off the most objects, or in Toon-Up mode, where the more times you hit the floating target (while the clock is ticking!), the more Laff Points are restored.
<li>Winning designs from the T-Shirt Design Contest - 3 US winners and 1 UK winner - are now available for purchase from Clarabelle's Cattlelog.
<li>Fixed an issue with the game sometimes incorrectly assigning DirectX 7 (which is no longer supported) and thus would result in the player unable to start Toontown.
<li>Toons now have shadows while walking on dirt paths in the Estate.
