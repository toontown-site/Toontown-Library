<b>January 12, 2007 [sv1.0.24.22]</b>
<ul>
<li>This is a maintenance release.
</ul>
<br>

<b>January 10, 2007 [sv1.0.24.21]</b>
<ul>
<li>Fixed a bug that caused the Grand Touring trophy to not be awarded if one or more of the races used were from a Grand Prix. Note the trophy would had been awarded after completing the next non-Grand Prix race. 
<li>Added several stability enhancements. 
<li>Fixed bug which kept a sound effect from playing when your Toon goes up in shovel skill level. 
</ul>
<br>


<b>December 6, 2006 [sv1.0.24.15.v2]</b>
<ul>
<li><b>Mac Only:</b> A new browser plug-in is available for installation.  Please login through the Toontown main website for automatic download and install.  This fixes a problem with proper Mac OSX version detection.
</ul>
<br>

<b>December 1, 2006 [sv1.0.24.15]</b>
<ul>
<li>Fixed a bug which would cause an exit crash if you tried to return your Doodle at the Doodle Shop, cancel, then exit the game.
<li>Fixed a bug that could cause crashes when you teleport around.
</ul>
<br> 


<b>November 30, 2006 [sv1.0.24.13.v2]</b>
<ul>
<li>Fixed a bug which would cause in game latency and instability whenever your Toon went to your Estate to visit with your Doodle, entered and exited your House, then teleported back to the playground.
</ul>
<b>Mac Only:</b>
<br><br>
A new Launcher and browser plug-in are available for installation. Please login through the Toontown main website and click the red "PLAY" button for automatic download and install.
<ul>
<li>The new plug-in will now properly minimize the web browser when you re-enter Toontown using the "Play" button on the Toontown webpage after exiting. 
<li>The new Launcher will fix a problem with the creation of the <i>Logs</i> folder which Toontown requires to run properly.
</ul>
<br>


<b>November 29, 2006 [sv1.0.24.13]</b>
<ul>
<li>Fixed a problem having to do with walking down streets and then teleporting back to the playground which could lead to game instability. 
<li>Made other fixes and changes which will enhance game stability.
</ul>
<b>Mac Only</b>
<br><br>
The new Launcher and browser plug-in are available for installation. Please login through the Toontown main website and click the red "PLAY" button for automatic download and install. 
<ul>
<li>Fixed a problem with the Launcher where the game installation music would continue after it had finished installing.
<li>Fixed a bug that caused you to not be able to re-enter Toontown via the "PLAY" button on the "Thanks for Playing" exit page after you've exited Toontown. 
<li>Fixed a bug that caused the Launcher to crash if you used a trial account, and to incorrectly treat your paid account as "trial" after you've paid for a subscription.
<li>The correct download music and movie showing download progress will now play in the Launcher.
<li>Made fixes pertaining to patching, logging, and error handling in the Launcher.
</ul>
<br>

<b>November 22, 2006 [sv1.0.24.10]</b>
<ul>
<li><b>Mac Only:</b> A new Launcher was released and will automatically install when you next login through the Toontown website.  It has a fix for "Player's Guide" button which will now correctly display the Player's Guide page in your web browser.
</ul>
<br>


<b>November 20, 2006 [sv1.0.24.10]</b>
<ul>
<li>Made the hardest parts of the Tug of War Trolley game easier.
<li>Fixed an incorrect camera angle for the Opera sound gag.
<li>Fixed problem with long text stepping over their word bubbles in the Cattlelog.
<li>Fixed a rare bug that caused the reward movie sequence for level 7 gags to not play correctly.
<li>Fixed a rare crash that occurs for the Catching Game.
<li>The Reward movie will no longer play if all Quests have been completed.
<li>Tweaked and improved the timing of sound FX and camera movements for the High Dive gag and all Sound gags.
<li>Fixed a problem Toons had with picking a Gag Tree while your gag inventory was full. It used to result in your Toon losing the gag off the tree, but now you won't be able to pick from a Tree (nor remove the tree) when your inventory is full.
<li>Made changes to gags so that Level 7 gags no longer count toward the maximum number you can have.
</ul>
<hr>



<b>October 25, 2006 [sv1.0.23.9]</b>
<ul>
<li>Stock up for battle with seven ultra-Toontastic new gags: High Dive, Train Track, Presentation, Opera Voice, Wedding Cake, Geyser, and Ocean Liner!
<li>Each Level 7 gag is unlocked once you've maxed out at 10,000 skill points on that gag track. 
<li>Unlike other gags, Level 7 gags are only awarded once you earn 500 additional skill points. 
<li>Each time you want to use a Level 7 gag, you will need to re-earn these skill points. 
<li>You can also plant one of these special gags in your Estate's garden.
</ul>
<hr>


<b>September 28, 2006 [sv1.0.22.17]</b><br/>
<ul>
<li>Fixed an issue with the big Watering Can that caused it to give an incorrect amount of water. 
<li>Added new SpeedChat phrase "Sorry, I'm busy gardening!" 
<li>Fixed a crash bug which could occur if another Toon is checking out your garden and you leave your Estate. 
<li>Fixed a bug that would occasionally cause a crash when you water a plant. 
<li>Fixed graphics associated with harvesting gags from a tree planted on a slope. 
</ul>


<b>September 14, 2006 [sv1.0.22.15]</b><br/>
<ul>
<li>Estate gardening is here - can you dig it?

<ul>
<li>Spruce up your Toon's estate with new gardening tools available in your next issue of Clarabelle's Cattlelog! Plant jellybeans around your house, add water, and watch 'em sprout. Once they've bloomed, pick 'em and sell 'em at the wheel-barrel to earn more jellybeans for planting! 
<li>The more you garden, the greater the variety of flowers you can plant. Advance your skills to purchase better gardening tools, earn trophies, and add to your total Laff Point level. Master gardeners can enhance their estates with statues and fountains available only from Clarabelle's Cattlelog. 
<li>And if you're running low on cream pies, plant a gag and watch a gag tree grow a stash of'em for you to pick and use in battle. Plant up to eight different trees around your estate. Plus, organically-grown gags have an advantage against the Cogs!
</ul>
</ul>

