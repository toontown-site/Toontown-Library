<b>March 1, 2007 [sv1.0.25.13]</b>
<br><br>
<b>Attention Toons over age 13!</b> Toontown is now integrated with the new <b><u>Disney XD</u></b> on Disney.com!
<br><br>
Here's what we need you to do to take advantage of the new features:
<ul>
<li>Log into <u>play.toontown.com</u> and verify your account name and password. It is possible that you may be prompted to choose a new account name. If so, please go ahead and complete that process. Do not start playing Toontown yet!
<li>Go to <a href="http://disney.go.com/dxd">Disney XD</a> on <a href="http://www.disney.com">Disney.com</a> and log in using your Toontown account name and password and create a D-Name.
<li>Set your chat permissions in DXD to allow True Friends (was Secret Friends) through the "Open Chat With Unlimited True Friends" option under the Communication Preferences Tab in your DXD Account Manager.  <b>*** Please Note ***</b> By default your new DXD account will have the same chat permissions as your Toontown account.
<li>Create and exchange DXD True Friend Codes (was Secret Friends) with people you know who have also created a D-Name. You'll then be able to Chat with your True Friends no matter where either of you are in the Disney.com network, including from within Toontown.
<li>Log into Toontown and chat with your new D-Name friends.
</ul>
For more details about D-Names, DXD, and chat permissions please visit:
<br><br>
<a href="http://home.disney.go.com/guestservices/faqs#community8">http://home.disney.go.com/guestservices/faqs#community8</a>
<br>
<br>
<br>
Once you have some True Friends, we want you to check out the new Friends List features.
<ul>
<li>Friends listed in green are SpeedChat friends, and those listed in blue are True Friends (was Secret Friends) who you can chat openly with.  This color scheme better matches the colors of the OpenChat and SpeedChat buttons.
<li>Player Friends will be highlighted in <b>BOLD</b> when they are logged into Toontown.
</ul>
The Friends List also has new tabs:
<ul>
<li>Online Toon Friends - This list only your Toontown friends who are currently online, by Toon name.
<li>All Toon Friends - This lists all of your Toontown friends, by Toon name.
<li>Online Player Friends . This list only your Disney.com account friends who are currently online, by D-Name.
<li>All Player Friends . This lists all of your Disney.com account friends, by D-Name.
<li>Each Friends List tab now sorts alphabetically within each color group.
<li>Toontown True Friends codes will now have a "TT" prefix to better differentiate them from DXD True Friends codes.
</ul>
Please try out the new features and let us know what you think!  Be sure to report any bugs via our <a href="http://play.toontown.com/reportBug.php">Report a Bug page</a>!
<br>
<br>
<br>
