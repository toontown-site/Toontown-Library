  All of the hard work on Cog HQ is starting to come together. We have started creating the interiors of the factory buildings. Each factory will house assembly lines where the Cogs are being manufactured. Toons will be sent on Cog part recovery missions to the factories in effort to stop the Cogs before they can propeller off into Toontown. 

    <br>
    <br>
    As we mentioned in the first issue of Backstage, Toons will work their way through each building, past a series of obstacles -- up and down elevator lifts, across conveyor belts, through doors -- while dodging heavy Cog-building machinery. We are in the process of building the obstacles within the factories and starting to get an idea of how the Toons will navigate their way through.<br>
