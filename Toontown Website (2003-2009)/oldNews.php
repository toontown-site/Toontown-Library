<b>May 23, 2006 [sv1.0.20.19]</b><br/>
<ul>
<li>Made changes which will make sure Toons receive the reward for defeating the Chief Justice.
<li>Corrected a few typographical errors
<li>Fixed a bug that caused problems with gift purchases and referrals for Toons with international characters in their names.
<li>Improved stability by eliminating the ability to skip certain battles in the DA's office.
<li>Improved game stability when interacting with the Mailbox in your Estate.
</ul>

<b>May 4, 2006 [sv1.0.20.18]</b>
<ul>
<li>We've made some enhancements to the Chief Justice battle.  Here are the details:
<ul>
<li>In the 9 Cogs difficulty case, a Toon's evidence will have a bonus weight of 1 for every juror after the first one is successfully put on the jury. For example, if a Toon were able to put 3 jurors successfully onto the jury, the total weight for her evidence would be 3 (1 + 2 bonus). During the bonus state (when all Cogs are stunned) this would double to 6!
<li>In the most difficulty case (10 Cogs!), the evidence bonus weight will be 1 for ever juror successfully put on the jury.  For example, if a Toon shot 4 jurors into the jury box successfully, the total weight of her evidence would be 5 (1 + 4 bonus).
<li>You can now click on other Toons in the Chief Justice battle to see their health, and the Friends Panel should also appear.
<li>The cannon ammo indicator during jury selection will now blink when you are about to run out of shots.
<li>The Chief Justice will perform his area attack less frequently.
<li>It should now be easier to hit the pan on the scales of justice when it is very low (i.e. when Toons are winning!).
<li>We've added a SpeedChat phrase for telling other Toons your evidence weight bonus during Chief Justice battle. If you have no bonus it will not be available.
</ul>
<li>Made changes to help correct some elevator woes inside the DA's Office Lobby.
<li>The damage of Sellbot HQ's Factory Goons has been decreased.
<li>Fixed the graphics issues with the snow flakes in The Brrrgh.
<li>The names of Toons with the Polar Bear effect should now be legible.
<li>Fixed a typo in Dr. Pullyurleg's dialogue when he offers you a new gag track to pick.
<li>Fixed a few crash causing bugs and improved general stability.
</ul>


