<h5 align="center">Welcome to Cog Headquarters (CogHQ)!</h5>

<p><b>Sellbot HQ</b>, attached to Daisy Gardens is the first Cog HQ to be released.
It has been very exciting for us to develop this new area of Toontown, and hopefully it is as fun for you to play in.  Cog HQ introduces new physics, environments, enemies, story elements, and game play.

<p><b>A Brief CogHQ Overview</b></p>

<p>Toons have been taken hostage by the Cogs! They are being held on the top floor of CogHQ. It is your mission to gang up and rescue them. Getting into CogHQ will not be easy though; their tight security will detect and reject any Toons that enter. You must build a Cog disguise first by recovering Cog parts from their factory. The factory is a 4-player environment with many new game-play elements. You can keep track of your Cog disguise progress with a new page in your Shticker Book.

<p>Once you have your entire Cog disguise built you can enter the Cog HQ lobby dressed as a Cog. The Sellbot Boss is a very busy Cog, so the guard will not allow you to see the Boss unless you have important Cog business, such as needing a promotion. You must earn your promotion by fighting Sellbots to collect "merits" around Toontown. Merits are given out after every battle just like Gag experience and are recorded in your Shticker Book as well. The elevator to the top of Cog HQ holds 8 players so when you are ready for your promotion, group up with your friends and head up to battle the Boss.

<p>If you defeat the Sellbot Boss Cog you will free the hostage Toon who will in turn present you with his "trading card". You can use these cards in battle to call on them for extra assistance in a tough spot. Of course, knowing the Cogs they will quickly build another Sellbot Boss Cog, so hurry and get your next promotion to rescue another Toon. There are 40 total promotions as you work your way up the corporate ladder.

