<p><h4>Toon Answers</p></h4>
<p><i><b><a name="create"></a>How do I create my Toon?</b></i></p>
<p>When you start into Toontown, the first step will be to create your Toon -- it's easy and fun, so just follow directions from your hosts, Mickey and Minnie Mouse.</p>

<p><i><b><a name="nametoon"></a>Can I name my Toon anything?</b></i></p>
<p>After creating your Toon, you have the option of either picking a name with help from the name generator, or creating your own name -— which will be submitted for approval by the Toon Council.  Toon names are limited to a maximum number of characters, and also to three words. We should make it clear that Toon names cannot be obscene or offensive. Duplicate names are possible within Toontown.</p>

<p><i><b><a name="howmanytoons"></a>How many Toons can I have?</b></i></p>
<p>Each Toontown account can have six Toons at any one time.  You can delete one of these six, if you would like to create another.  But once you delete a Toon, everything associated with that Toon -- friends, clothes, gags, and everything -- will be erased.</p>

<p><i><b><a name="clothes"></a>Can I get special clothes for my Toon?</b></i></p>
<p>You can dress your Toon lots of different ways when you create your Toon. After completing certain ToonTasks, you may be rewarded with a clothing ticket which you can use at Clothing Stores in each playground if you decide to change your outfit.</p>

<p><i><b><a name="givetoon"></a>Can I give my Toon to someone else?</b></i></p>
<p>No. Your Toon is yours and cannot be traded.</p>

<p><i><b><a name="bothonline"></a>If I have more than one Toon, can they both be active in Toontown at the same time?</b></i></p>
<p>No.  Only one person on each Toontown account can be logged into Toontown at any time, and you can only have one Toon at a time active in Toontown.</p>

<p><i><b><a name="controltoon"></a>How do I control my Toon? With a keyboard, mouse, joystick, or a combination?</b></i></p>
<p>Use the arrow keys on the keyboard to move your Toon around Toontown, and use the mouse to click on screen options or menu choices. You don't need a joystick to play.</p>

<p><i><b><a name="view"></a>How do I change my viewpoint?</b></i></p>
<p>The Tab key on your keyboard will let you switch your camera view of Toontown.  You can also use the 'page up' and 'page down' keys to look up and down.</p>

<p><i><b><a name="tooninfo"></a>How do I get information about my own Toon?</b></i></p>
<p>In the lower left corner of the screen, you'll see a "Laffmeter" that shows you how healthy your sense of humor is.  Also, the "Shticker Book," which appears in the lower right corner of the screen, has information about your accomplishments in the game, and what kind of Gags you have earned to fight the Cogs. You can click on the corners of the Shticker Book to flip through the pages.</p>

<p><i><b><a name="othertooninfo"></a>How do I get information about another Toon?</b></i></p>
<p>You can click on a Toon's name to learn more about that Toon.  By clicking on that Toon's name, you also have the option to add that Toon to your Friends List.  Once someone is on your Friends List, you can teleport to that person, or call that friend for help in a battle.  It's easy to change your Friends List--you always have the option to add or remove another Toon.  You can also talk to other Toons -- via SpeedChat -- to find out what their ToonTasks are.</p>

<p><i><b><a name="namesappear"></a>What are those names that appear on the screen -- are they other players?</b></i></p>
<p>Names with arrows appear onscreen indicating other Toons or buildings that are nearby, but not within view.</p>

<p><i><b><a name="otherstars"></a>Why do some Toons have stars over their names?  Can I have one too?</b></i></p>

<p>Stars are awarded to Toons who are especially proficient at rescuing buildings from the Cogs.  When you have helped to rescue at least five Cog buildings, and for as long as those buildings continue to remain safe from the Cogs, you will be awarded a bronze star.  You'll earn a silver star for rescuing even more buildings, and it's a rare Toon indeed who can rescue enough buildings to earn a gold star.</p>

<p>But you have to stay vigilant!  If the Cogs capture back enough of the buildings you rescued, you'll lose your star!  If this happens, you'll have to go out and rescue some more buildings to get your star back.</p>

<p>Note that all the buildings you rescue must be on the same district in order to count towards your star.  (See the "Districts" page in your <a href="#book">Shticker Book</a>.)  Stars are specific to the Toontown district you play on.  If you switch to another district, you will leave your star behind.</p>

<p><i><b><a name="mystardisappeared"></a>I had a star over my name, but now it's gone!  What happened?</b></i></p>

<p>There are two ways you can lose your star:</p>
<ul>
  <li>Maybe the Cogs took back one or more of the buildings you had helped to rescue, dropping your total number of buildings rescued below five.  This can even happen while you are not logged on, since those villainous Cogs never sleep.  The only thing you can do is stay vigilant, and keep rescuing buildings!</li>
  <li>Maybe you recently switched to another district.  There are multiple districts within Toontown (see the "Districts" page in your <a href="#book">Shticker Book</a>), and each district is facing its own onslaught of Cogs.  You might have switched districts by picking another district from your Shticker Book, or by going to visit a friend who was in another district.  But the star you earned is specific to the district in which you rescued the buildings.  If you switch to another district, your star goes away; but you'll get your star back when you return (unless the Cogs took back several of your buildings while you were gone).</li>
</ul>

<p><i><b><a name="savegame"></a>How do I save my game?</b></i></p>
<p>Everything about your Toon -- looks, things you have collected, points earned, gags bought, Cogs defeated -- is automatically saved, so there's no need for you to do anything.</p>

<p><i><b><a name="exitgame"></a>How do I exit Toontown, and will my Toon still be there when I come back?</b></i></p>
<p>At the lower right corner of the screen, you'll see your "Shticker Book."  Turn to the first page, which is called "Options," and from there hitting "Exit Toontown" will take you back to your "Pick-a-Toon" stable of characters, where you can Quit completely or log out. Don't worry though, when you come back, your Toon or Toons will be waiting for you.</p>

