<p><i><b><a name="secrets"></a>How do I chat freely with my real-life friends?  What is the Secret Friends Feature?</b></i></p>

<p>In order to use the Secret Friends feature, it must first be enabled by the Account Holder 
using the Parental Controls.</p>

