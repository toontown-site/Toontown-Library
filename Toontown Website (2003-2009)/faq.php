<p><a name="speed"></a><i><b>How fast an Internet connection do I need to play Toontown?</b></i></p>
<p>The faster the better, but Toontown is designed to work over even a 56K modem.</p>

<p><a name="adv"></a><i><b>Will someone with a faster Internet connection have an advantage playing?</b></i></p>
<p>No.  Toontown has been designed to work online so that everyone can
play together and compete with each other in the game.  The primary
benefit of a fast connection is that you won't have to wait as long
for the initial download.</p>

<p><a name="long"></a><i><b>How long does it take to download Toontown, and will I have to download it more than once?</b></i></p>
<p>Normal installation should take 10 to 20 minutes, depending on the speed of your Internet connection.  Occassionally there will be updates to Toontown that will have to be downloaded as well. These updates should download fairly quickly.</p>

<p><a name="nt"></a><i><b>Can Toontown run under Windows XP or Windows 2000?</b></i></p>
<p>Toontown works with Windows 98, ME, NT 4.0, 2000, and XP.  On NT/2000/XP,
you must install Toontown from an account with administrator privileges, but
administrator privileges should not be required to run Toontown unless
downloading an updated Toontown Launcher ActiveX control is required, and
in that case the user will be notified.  Other than that, a WinXP account
with <I>Limited User</I> privileges should be able to run Toontown
successfully.  Toontown will run on NT 4.0 if your video driver supports OpenGL acceleration.</p>

<p><a name="other_sys"></a><i><b>Can I play Toontown in operating systems other than Windows?</b></i></p>
<p>Not at this time, but we are open to requests.</p>

