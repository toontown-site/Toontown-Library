<p><h4>Cog Answers</p></h4>
<p><i><b><a name="cogs"></a>What are Cogs, and what are they doing here?</b></i></p>
<p>Cogs are part of an invention that has gone terribly wrong. They are turning Toontown into a black-and-white metropolis of skyscrapers and businesses -- it is up to every Toon to find a way to battle them and ultimately stop them.</p>

<p><i><b><a name="diffcogs"></a>How many different Cogs are there, and do different Gags work differently on them?</b></i></p>
<p>There are lots of different Cogs, but the main point is not that different Gags work differently on them, but that there are unique combinations of Gags that are more effective.  For example, if you use a "Trap" Gag, such as a banana peel, you'll want to use a "Lure" Gag such as money to get the Cog to step forward and slip on the peel. Rolling your mouse over the various gags in your inventory will help you understand the relative strengths and weaknesses of each of them.</p>

<p><i><b><a name="battleprimer"></a>How does 'Battle' work?</b></i></p>
<p>Robot Cogs are thwarted by your using Gags on them.  You can fight Cogs alone, or with a group of Toons. Once in battle you can:</p>
<ul><li>Attack -- use a Gag on the Cog</li>
<li>Heal -- use a healing Gag on another Toon in the battle.</li>
<li>Call for help -- ask friends to help you in battle by sending them an 'SOS'</li>
<li>Run -- if you think you are in over your head during a street battle, you can run back to the nearest Playground</li></ul></p>

<p><i><b><a name="cogtypes"></a>What is a 'Sellbot', 'Cashbot', etc?</b></i></p>
<p>Sellbots are cogs that are experts in the art of marketing.  The 'Telemarkter' is one example of a Sellbot. Cashbots are primarily concerned with monetary issues.  Every Cog has their type displayed over their head with their name. If you aren't sure what type of Cog you are dealing with, you can click on one of them in streets and a detail panel will give you more information about which Cog "family" they belong to.</p>

<p><i><b><a name="battle"></a>How do I keep the Cogs from taking over Toontown?</b></i></p>
<p>Robot Cogs are thwarted by your using Gags on them.  You can fight Cogs alone, or with a group of Toons. Once in battle you can:</p>
<ul><li>Attack - use a Gag on the Cog.</li>
<li>Toon-up - use a Gag to refill another Toon's Laffmeter in the battle.</li>
<li>Call for help - ask friends to help in battle by sending them an "SOS.</li>
<li>Run - teleport back to the playground.</li></ul></p>

<p><i><b><a name="cogbuilding"></a>How can I tell what kind of Cogs work in a Cog building?</b></i></p>
<p>Cog buildings are plainly labeled with signs above the door. You can also hang around the building awhile and notice the Cogs that go in and out. Also, notice that not all Cog buildings look alike.</P>

<p><i><b><a name="gagcombo"></a>Does the gag I use affect other toon's gag attacks?</b></i></p>
<p>There are unique combinations of Gags that are more effective.  For example, if you use a Trap Gag, such as a banana peel, you'll want to use a Lure Gag such as money to get the Cog to step forward and slip on the peel.</p>

<p><i><b><a name="secretbattle"></a>Are there secret battle sequences that will earn me extra points?</b></i></p>
<p>If multiple Toons select the same Gag, the combination makes for a more powerful attack. Similarly, luring Cogs closer before springing a Gag on them will result in a more powerful attack.</p>

<p><i><b><a name="lose"></a>What happens if I lose a battle?</b></i></p>
<p>If your Laffmeter hits zero, your Toon is sent to the nearest playground for recovery. You will also lose all the Gags you were carrying. After you lose a battle, hang around the playground until you heal and play Trolley Games to restock your Gag inventory.</p>

<p><i><b><a name="die"></a>Can my Toon ever die?</b></i></p>
<p>No.  Toons never die. They just lose their sense of humor for a little while.</p>

<p><i><b><a name="heal"></a>How do I "heal" my Toon or refill my Laffmeter?</b></i></p>

<p>Another Toon can use a Toon-up Gag on you when in battle.  You recover quickest by collecting special treasures in the playgrounds, like the starfish in Donald's Dock.  Finally, your Laffmeter will slowly fill back up all by itself whenever you spend time in a playground or playing Trolley Games.</p>

