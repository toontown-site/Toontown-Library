<p><h4>Technical Answers</p></h4>

<p><a name="computer"></a><i><b>What kind of computer is needed to play Toontown?</b></i></p>
<p>You must have a PC -- Toontown does not run on Macintosh computers or on Netscape browsers.  You also need:
<ul>
<li>Microsoft Windows 98, ME, 2000, or XP</li>
<li>233 MHz or faster CPU</li>
<li>128MB RAM memory (256MB Recommended)</li>
<li>100MB free space on your "\Program Files" hard drive (most likely C:)</li>
<li>A video card with 3-D acceleration, at least 8MB (32MB/AGP card is recommended)</li>
<li>A 56K modem, or a faster Internet connection</li>
<li>Microsoft DirectX 7.0 (or newer)</li>
<li>Microsoft IE v. 4.0 (or newer)</li></ul></p>

