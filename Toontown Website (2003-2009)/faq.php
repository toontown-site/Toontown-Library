<p>SpeedChat is a point-and-click chat system that lets you talk with other Toons by selecting canned phrases from a preset menu.</p>

<p><i><b><a name="secrets2"></a>What are "Secrets?"</b></i></p>
<p>Secret passwords are used in the Secret Friends feature of the game and enable you to chat freely with another Toon. You can set this up with your "real- life" friend so you can talk online. A Secret Password is requested from the Friends/Secret panel in the upper right corner of the screen. This password must be communicated outside of the game (to ensure safety). Once the password has been generated in-game and delivered to friend A, it must be entered in-game by friend B within 48 hours or the password will expire. Each password is unique and can be used with only 1 friend. To openly chat with 2 or more friends, a secret password must be created for each friend.</p>

<p><i><b><a name="hearsomething"></a>What's happening when other Toons "talk" and what I see/hear is something that doesn't make sense?</b></i></p>
<p>The other players are chatting using the keyboard (instead of SpeedChat), but since the Secret Friends feature is not enabled between you, all you will hear/see is "meow, meow, meow" or "bark, bark, ruff" (depending on the species).</p>

<p>If you think the other player is trying to talk to you, there are selections in SpeedChat that let you suggest they use that (SpeedChat) to communicate with you.</p>

<p><i><b><a name="filter"></a>Is there a bad word filter in Toontown?</b></i></p>
<p>Yes. Obscenities are filtered from chat messages in Toontown.</p>

<p><i><b><a name="whisper"></a>Can I talk to just one Toon?</b></i></p>
<p>Yes. After clicking on the name (on-screen or in your Friends list) of the Toon you'd like to message, select "Whisper." A special pop-up window will appear with the chat buttons. Use the appropriate button (depending on account permissions) to send a "Whisper."  These messages are sent directly to that Toon, and are not broadcast to others nearby.</p>

<p><i><b><a name="meetup"></a>How do I meet up with a friend somewhere in Toontown?</b></i></p>
<p>Your Friends List tells you which of your friends are online or offline-it keeps you informed about who is currently playing and who isn't. Click on that person's name and choose "Go To" to be sent to their location. You will also get a notification bubbles on your screen whenever a friend logs on or off the game, or is teleporting to you.</p>

