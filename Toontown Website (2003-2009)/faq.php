<p><h4>General Answers</h4></p>

<p><a name="whatis"></a><i><b>What is Disney's Toontown Online?</b></i>
<p>Disney's Toontown Online is the first massively multiplayer online PC game for ages seven and older.  We are giving a special sneak preview to a limited amount of members before we officially release the product.</p>

<p><a name="release"></a><i><b>When will Disney's Toontown Online be available for everyone?</b></i></p>
<p>A release date has not been set, but Charter Members will be the first to know.</p>

<p><a name="rpg"></a><i><b>What is an online multiplayer RPG?</b></i></p>
<p>In role-playing games (RPGs), you create a character and then step into a game world with that character.  Taking an RPG online allows you to play with others -- that's why it's called multiplayer!</p>

<p><a name="todo"></a><i><b>What am I supposed to do in Toontown?</b></i></p>
<p>Players create their own cartoon characters and enter Toontown to:</p>
<ul><li>Meet friends and make new friends</li>
<li>Compete in games</li>
<li>Battle the evil robot Cogs</li></ul></p>

<p><a name="cost"></a><i><b>How much does it cost to play Toontown?</b></i></p>
<p>When you sign up, you will have a free trial period in which you can experience Toontown at no cost to you. After your free trial period, if you choose to subscribe, a $9.95 Membership fee will be billed on your credit card and a monthly fee of $5.95 will be billed thereafter. This is a SPECIAL price for Charter Members only.</p>

<p><a name="age"></a><i><b>What is the appropriate age for Toontown?</b></i></p>
<p>Toontown is for kids ages seven and up.</p>

